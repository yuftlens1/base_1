'''
认识数据类型
'''
num1 = 1                                          #int整型；整数
num2 = 1.11                                       #float浮点型；小数
a = 'hello word'                                  #str 字符串  加引号
b = True                                          #bool 布尔值
c = [10,20,30,40]                                 #list  列表 [ ]
d = (10,20,30,40)                                 #tuple 元组 ( )
e = {10,20,30}                                    #set   集合 { }
f = {'name':"二蛋","age":'109','xb':'男'}          #dict  字典
print(type(num1))
print(type(num2))
print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))
print(type(f))